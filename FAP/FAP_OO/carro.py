class Carro:
    def __init__(self, marca, modelo, ano):
        self.marca = marca
        self.modelo = modelo
        self.ano = ano
        self.velocidade = 0

    def exibir_infos(self):
        print(f"{self.marca} {self.modelo} {self.ano}")



def acelerar(self):
    self.velocidade = self.velocidade + 5


def frear(self):
    self.frear = self.frear + 5